    public void showProfile() {
        try {
            Statement statement;
            statement = conn.createStatement();
            ResultSet resultSet = statement
                    .executeQuery("SELECT * FROM STUDENT where StudentID='" + idS + "'");
            while (resultSet.next()) {
                String a = resultSet.getString("StudentID");
                String b = resultSet.getString("St_Name");
                //String cc = resultSet.getString("Date_of_Birth");
                int d = resultSet.getInt("current_Semester");
                String h = Integer.toString(d);
                int g = resultSet.getInt("current_Year");
                String i = Integer.toString(g);
                String e = resultSet.getString("Department");
                String f = resultSet.getString("Section");
                idLabel.setText(a);
                nameLabel.setText(b);
                semLabel.setText(h);
                deptLabel.setText(e);
                yrLabel.setText(i);
                secLabel.setText(f);

            }

        } catch (SQLException ex) {
            Logger.getLogger(Gridview1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

