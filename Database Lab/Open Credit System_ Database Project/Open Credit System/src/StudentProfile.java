    private void selectCourseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectCourseBtnActionPerformed
        // TODO add your handling code here:
        int sem = 0, yr = 0;
        try {
            Statement statement;
            statement = conn.createStatement();
            ResultSet resultSet = statement
                    .executeQuery("SELECT * FROM STUDENT where StudentID='" + idS + "'");
            while (resultSet.next()) {
                sem = resultSet.getInt("current_Semester");
                yr = resultSet.getInt("current_Year");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Gridview1.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (sem == 1 && yr == 1) {
            this.setVisible(false);
            CourseSelection11 c = new CourseSelection11(idS);
            c.setVisible(true);
            c.showTableData();
            this.dispose();
        } else {
            this.setVisible(false);
            CourseSelection c = new CourseSelection(idS);
            c.setVisible(true);
            c.showTableData();
            this.dispose();
        }

    }//GEN-LAST:event_selectCourseBtnActionPerformed

