;R2+G(ON)-G(OFF)-Y(ON)-R2(OFF)-R1(ON)

LED24 SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:LED24
ORG 1000H

START:
  
  
  ;TURN ON CONTROL REGISTER
  MOV AL,80H
  OUT 1FH,AL

  ;TURN OFF SEGMENT DISPLAY
  MOV AL,0FFH
  OUT 19H,AL

  ;RED2+GREEN TURNS ON SIMULTANEOUSLY
  MOV AL,0AH
  OUT 1BH,AL

  ;DELAY
  MOV CX,0FFFFH
  L0:LOOP L0
  
  ;DELAY
  MOV CX,0FFFFH
  L1:LOOP L1
  
  ;DELAY
  MOV CX,0FFFFH
  L2:LOOP L2

  ;DELAY
  MOV CX,0FFFFH
  L3:LOOP L3

  ;GREEN TURNS OFF,RED2 STILL ON
  MOV AL,08H
  OUT 1BH,AL

  ;DELAY
  MOV CX,0FFFFH
  L4:LOOP L4
  
  ;DELAY
  MOV CX,0FFFFH
  L5:LOOP L5
  
  ;DELAY
  MOV CX,0FFFFH
  L6:LOOP L6

  ;DELAY
  MOV CX,0FFFFH
  L7:LOOP L7

  ;YELLOW TURNS ON,RED2 STILL ON
  MOV AL,0CH
  OUT 1BH,AL

  ;DELAY
  MOV CX,0FFFFH
  L8:LOOP L8
  
  ;DELAY
  MOV CX,0FFFFH
  L9:LOOP L9
  
  ;DELAY
  MOV CX,0FFFFH
  L10:LOOP L10

  ;DELAY
  MOV CX,0FFFFH
  L11:LOOP L11

  ;RED2 TURNS OFF,YELLOW STILL ON
  MOV AL,04H
  OUT 1BH,AL     
  
  ;DELAY
  MOV CX,0FFFFH
  L12:LOOP L12
  
  ;DELAY
  MOV CX,0FFFFH
  L13:LOOP L13
  
  ;DELAY
  MOV CX,0FFFFH
  L14:LOOP L14

  ;DELAY
  MOV CX,0FFFFH
  L15:LOOP L15

  ;RED1 TURNS ON,YELLOW STILL ON
  MOV AL,05H
  OUT 1BH,AL

LED24 ENDS
END START
  

  

  
 
