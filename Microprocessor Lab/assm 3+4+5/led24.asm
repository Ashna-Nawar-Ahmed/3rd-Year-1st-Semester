;R2+G(ON)-G(OFF)-Y(ON)-R2(OFF)-R1(ON)

LED24 SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:LED24
ORG 1000H

START:

;TURN ON CONTROL REGISTER
MOV AL,80H
OUT 1FH,AL


;TURN OFF SEGMENT DISPLAY
MOV AL,0FFH
OUT 19H,AL

;LOOP CONTROL VARIABLE
MOV BX,05H

;SHOWING OUTPUTS
MOV SI,OFFSET ARRAY
L1:MOV AL,BYTE PTR CS:[SI]
OUT 1BH,AL

;DELAY
MOV CX,0FFFFH
L2:LOOP L2

;DELAY
MOV CX,0FFFFH
L3:LOOP L3

;DELAY
MOV CX,0FFFFH
L4:LOOP L4 

;DELAY
MOV CX,0FFFFH
L5:LOOP L5

;INCREMENT
INC SI
DEC BX
CMP BX,0H
JZ LOOPOUT
JMP L1

ARRAY:

;R2+G(ON)
DB 0AH
;G(OFF),R2 STILL ON
DB 08H
;Y(ON),R2 STILL ON
DB 0CH
;R2(OFF),Y STILL ON
DB 04H
;R1(ON),Y STILL ON
DB 05H


LOOPOUT:
LED24 ENDS
END START

